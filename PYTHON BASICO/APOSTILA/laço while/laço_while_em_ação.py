#INTRODUÇÃO AOS LAÇOS WHILE
"""
O laço for toma uma coleção de itens executa um bloco de código uma vez para cada item da coleção,
    Em comparação, o laço WHILE executa durante o tempo em que, ou ENQUANTO, uma determinada condição for verdadeira
"""
"""
Ex: usando o laço while para contar uma serie de numeros
"""
number = 1
#enquanto a variavel for menor que 5
while number <= 5:
    print(number)

    number += 1

"""
O código no laço exibi o valor de number(variavel) e então soma 1 a esse valor com number += 1
"""
