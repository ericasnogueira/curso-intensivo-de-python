"""
Podemos usar a instrução del para remover totalmente um par-chave.
Tudo que del precisa é do nome do dicionario e da chave que deseja
remover
Ex : remover a chave 'points' do dicionario

"""
alien_0 = {'color':'green', 'pontos': 5}
print(alien_0)

#removendo
del alien_0['pontos']
#mostrando alien sem o ponto
print(alien_0)

# o par chave-valor apagado é removido de forma a permante


